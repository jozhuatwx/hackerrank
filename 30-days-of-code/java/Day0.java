import java.util.*;

class Day0 {
    public static void main(String[] args) {
        // print hello world
        System.out.println("Hello, World.");

        // create scanner
        Scanner in = new Scanner(System.in);

        // read input
        String input = in.nextLine();

        // close scanner
        in.close();

        // print input statement
        System.out.println(input);
    }
}
